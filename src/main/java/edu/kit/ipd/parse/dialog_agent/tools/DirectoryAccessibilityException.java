package edu.kit.ipd.parse.dialog_agent.tools;

public class DirectoryAccessibilityException extends RuntimeException {

	private static final long serialVersionUID = 1109123210620785351L;
	
	public DirectoryAccessibilityException() {
		super();
	}

	public DirectoryAccessibilityException(String message) {
		super(message);
	}

	public DirectoryAccessibilityException(String message, Throwable cause) {
		super(message, cause);
	}

	public DirectoryAccessibilityException(Throwable cause) {
		super(cause);
	}
}
